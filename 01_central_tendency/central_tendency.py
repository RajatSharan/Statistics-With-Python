# Measure of Central Tendency 

# Mean,Median,Mode

age=[10,20,30,40,50,60,70,80,12,35,56,76,43,86,200,20]

#to find out the mean

import numpy as nm

print(nm.mean(age))

#to find out the median

print(nm.median(age))

import statistics as sts 

print(sts.mean(age))
print(sts.median(age))
print(sts.mode(age))


